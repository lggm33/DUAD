# Suma
suma = 1 + 1
print("1 + 1 =", suma)

# Resta
resta = 5 - 3
print("5 - 3 =", resta)

# Multiplicación
multiplicacion = 2 * 4
print("2 * 4 =", multiplicacion)

# División
division = 10 / 2
print("10 / 2 =", division)

# División entera
division_entera = 10 // 3
print("10 // 3 =", division_entera)

# Módulo (resto de la división)
modulo = 10 % 3
print("10 % 3 =", modulo)